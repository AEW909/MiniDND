const THIRD_CASTER_SUBCLASSES = ['Eldritch Knight', 'Arcane Trickster']

export function getSpellSlots(
  className: string,
  subclass: string | null,
  level: number
): SlotRow {
  const idx = Math.min(Math.max(level, 1), 20) - 1

  if (className === 'Warlock') return WARLOCK[idx]
  if (className === 'Artificer') return ARTIFICER[idx]
  if (['Bard', 'Cleric', 'Druid', 'Sorcerer', 'Wizard'].includes(className)) return FULL_CASTER[idx]
  if (['Paladin', 'Ranger'].includes(className)) return HALF_CASTER[idx]
  if (subclass && THIRD_CASTER_SUBCLASSES.includes(subclass)) return THIRD_CASTER[idx]
  return [0, 0, 0, 0, 0, 0, 0, 0, 0]
}

export function isCasterClass(className: string, subclass: string | null): boolean {
  return getSpellSlots(className, subclass, 3).some(s => s > 0)
}

export function slotLevelLabel(level: number): string {
  const labels = ['', '1st', '2nd', '3rd', '4th', '5th', '6th', '7th', '8th', '9th']
  return labels[level] ?? `${level}th`
}
